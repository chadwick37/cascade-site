	<h3>Our mills ship quality Alder and Maple domestically, and worldwide</h3>
	<p>Cascade and Port Angeles ships by truck, rail and container. Overseas shipping is made easier thanks to our proximity to the major local ports of Seattle, Tacoma and Portland. We have a great deal of experience in multi-modal container shipping to Europe and Asia.</p>
	<p>Full and partial loads are handled quickly and professionally by our staff. And don&rsquo;t forget to take a peek at the Cascade Unit Puller - a proprietary Cascade tool that makes removing heavy lumber bundles from shipping containers, trailers, rail cars and other hard to access areas quick, safe and easy.</p>
