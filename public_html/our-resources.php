<h3><img alt="" src="/sites/default/files/content/alder-sample.jpg" style="padding-right: 20px; padding-bottom: 20px; float: left; width: 192px; height: 224px; " />Common uses<img alt="" src="/sites/default/files/content/alder-sidebar.png" style="padding-left: 20px; float: right; width: 298px; height: 806px; " /></h3>
<p>Alder is a fantastic interior finish hardwood, and a stable frame and utility choice for furniture construction. You commonly see Alder used for cabinet doors, window construction, beam wraps, interior doors, trims, and mouldings. Alder is also used for guitar blanks, childern&#39;s toy construction, among many other applications.&nbsp;</p>
<h3>&nbsp;</h3>
<h3>Finishing and staining</h3>
<p>Of special interest is the superb finishing versatility of alder. Both the heartwood and sapwood are a light honey color. This light and uniform color is both easy to stain and highly valued in its natural state for today&#39;s clean modern designs. If staining is desired, Alder can easily match Cherry, Walnut, Pecan, Maple, Mahogany and many others. And since sap discoloration and mineral streaks are absent, Alder is easy to finish with clear &#39;fruitwood&#39; stains.</p>
<h3>A specialty product<img alt="Alder grows only in the Pacific Northwest" src="/sites/default/files/content/alder-map.png" style="float: right; width: 244px; height: 276px; " /></h3>
<p>Alder commercially grows only in the Pacific Northwest. Resource Managers have the opportunity to grow a product that will not have competition from other countries with less expensive management and harvest costs. This distinction between Douglas Fir and Hemlock, which are commodity products, and Alder, a specialty product, is why Alder prices have increased over the past 10 years while Douglas Fir and Hemlock prices decline.</p>
      
