<h2>Maple Hardwood</h2>
<p>Our Pacific Coast Maple lumber is widely used in furniture, cabinet, casegood, flooring and specialty applications. Maple is used in both very modern and traditional styles. Use of maple for kitchen cutting boards and butcher blocks testifies to the rugged utility of this hard and fine grained wood.</p>
<h3>Workability</h3>
<p>Pacific Coast Maple &ndash; or Big Leaf Maple &ndash; is well known for its working properties. It is moderately hard and of medium strength. It turns, planes and shapes well and can be sanded to a smooth finish. The wood glues easily, has moderate nail and screw holding ability, and takes a good finish..</p>
