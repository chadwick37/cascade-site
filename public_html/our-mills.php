	<h3>Logs are the foundation of our company</h3>
	<p>Cascade Hardwood receives logs from private ownership as well as from contracts with large timber owners and state ownership.</p>
	<p>All logs are first weighed, then transported to the log yard where logs are unloaded and rolled out to be individually inspected and measured.</p>
	<p>Then the logs are placed on the merchandising deck to be debarked and chopped into appropriate lengths. Then the logs head off inside the sawmill to get cut for maximum yield.</p>
	<div class="video-clips"><video controls="controls" height="234" poster="/sites/default/files/content/cascade_mini_tours1.jpg" preload="none" src="/sites/default/files/videos/mini_tour1.mov" tabindex="0" type="video/mp4" width="416"> </video></div>
</div>
<div class="left">
	<h3>The magic happens inside the Saw Mill</h3>
	<p>Once the logs are sent to the sawmill headrigs they get loaded onto a carriage and rotated to make an opening cut on the side with the best grade. The rotary gang saw takes the two-sided cant from the headrigs and boxes the heartwood, separating out the low-grade wood into a mini cant. Then all lumber is then hand-sorted and distributed to either the board edger or trim saws.</p>
	<p>Automated Board Edging uses Recovery Data from the Scanner to get the Best Edge Cuts and careful board length trimming begins to define the final piece of lumber.</p>
	<div class="video-clips"><video controls="controls" height="234" poster="/sites/default/files/content/cascade_mini_tours2.jpg" preload="none" src="/sites/default/files/videos/mini_tour2.mov" tabindex="0" type="video/mp4" width="416"> </video></div>
</div>
<div class="right">
	<h3>Conditioning and drying are important technical processes</h3>
	<p>After the lumber is sorted, stickered, and stacked it conditions outside for a minimum of three days so as to achieve it&rsquo;s very desirable natural golden honey color. The lumber, stacked three units high, is then loaded into one of our many large kilns.</p>
	<p>Once the appropriate moisture content is reached the automated controls start the lumber conditioning and equalizing cycles, then shut down the kiln at the proper time.<br />
		<br />
		&nbsp;</p>
	<div class="video-clips"><video controls="controls" height="234" poster="/sites/default/files/content/cascade_mini_tours3.jpg" preload="none" src="/sites/default/files/videos/mini_tour3.mov" tabindex="0" type="video/mp4" width="416"> </video></div>
</div>
<div class="left">
	<h3>Getting you the best value possible</h3>
	<p>After seasoning and drying the two-staged sanding process begins giving the product its desired consistency. Then our skilled graders visually inspect both sides of each board, looking for the percentage of yield and defect and grading the board for unique character attributes.</p>
	<p>Each board is stamped or marked with the appropriate grade markings. The lumber is then sorted by grade and length before packaging. Regular checks and continuous training ensure that grading is consistent and accurate.<br />
		&nbsp;</p>
	<div class="video-clips"><video controls="controls" height="234" poster="/sites/default/files/content/cascade_mini_tours4.jpg" preload="none" src="/sites/default/files/videos/mini_tour4.mov" tabindex="0" type="video/mp4" width="416"> </video></div>
