			<h2>Alder &ndash; The Versatile Hardwood<img alt="" src="/sites/default/files/content/alder-tile.jpg" style="float: right; width: 313px; height: 324px; " /></h2>
<p>For workability, availability, and finishing, Alder is a very versatile and predictable material. It can be used alone or together with other species. Alder is a close or fine grained wood similar to cherry, birch and maple. It is used in both solid wood and veneer applications. Alder&#39;s fine homogeneous grain structure and the light uniform color of both heartwood and sapwood make it easy to form, fasten and finish.</p>
<h3>Workability</h3>
<p>This comparison chart shows that the total workability index of Alder is higher than most hardwoods. As you will see, Alder is an excellent choice for even the most complex machining, gluing and finishing projects.<br />
	<br />
